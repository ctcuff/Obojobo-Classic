Additional Options
===================
By default, users with the co-developer and observer roles will not be able to publish instances of the object (if that object is a master).  You can change this setting by granting the right to publish instances.  Note that owners can always publish instances.

Adding Collaborators
====================

One of the powerful Obojobo features is the ability to collaborate with your colleagues and designers. That is, you have the ability to add collaborators for the authoring of objects.  Note that (as stated in the above section) you will need to be an owner to add or remove collaborators or modify permissions.

To add collaborators to your object:

1. Login into the Obojobo repository
1. In the 'My Objects' section, selected an object
1. Click on 'Share'
1. Click on the 'Add Collaborators' button in the Permissions window.
1. Select the name of the collaborator in the "Users to Add" list, then click the "Add" button.  This will bring the selected user over to the "Collaborators" list on the right.  When your finished adding users, click 'Next'.
1. Select the permission level you want to give to the collaborators by clicking on the radio button. If you select "Co-Developer" or "Observer" you will be given the  additional option of allowing the collaborator to be able to publish instances.  By default this setting will be unchecked.

Removing Collaborators & Modifying Permissions
==============================================
1. Select the desired object from the 'My Objects' section
1. Click on 'Share'
1. Select the name of the user you wish to modify the permissions for from the collaborators list.
1. Change the new permission level for that user.  Select 'None' to revoke all permissions and remove them completely as a collaborator.

**Tip**: Adding and Removing can be done in groups.  You can select multiple users by shift-selecting.  Control clicking also works.

