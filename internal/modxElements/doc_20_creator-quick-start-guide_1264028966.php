Content Creator Quick Start Guide
=================================

About
=====

This guide is designed to provide an overview of the learning object creation process to get you started with Obojobo quickly. Detailed information and step-by-step instructions are available in the [Creating Learning Objects]() section. 

What does Obojobo do?
=====================

[Obojobo]([~8~]) is a management system to assist you in the creation and administration of [learning objects]([~13~]). With Obojobo, your students have access to purposeful and concise instructional pieces you design using the Obojobo editor. As the author of learning objects, you are able to view your students' performance, the time your students took to practice, share your objects with your colleagues for their use and many other neat tricks to help students in their learning endeavor.

What do I do as a Content Creator?
==================================

As a content creator you have the ability to author instructionally sound learning objects as well as manage and share them with your colleagues. The Repository and Editor are the two interfaces you will use to create and manage your learning objects. The power of Obojobo is the ability to author re-useable instructional components you create once and implement in different courses or instructional scenarios. 

Learning objects
================

Refer to [this article]([~13~]) for a complete explanation of learning objects.


Logging-in
==========

Refer to the [FAQ on Accounts]([~16~]) for information on logging in.

Creating objects
================

You will start by creating a draft object. Drafts are works-in-progress. 

1. Login into the Obojobo repository with your credentials
2. In the 'My Objects' section, click on "Start New Draft" under the "Drafts" sub-tab to launch the Obojobo editor. 
3. The Editor will launch in a separate window. The editor is divided into seven primary sections which will guide through the process of creating your draft learning object. Each section in the editor is intended to do the following: 
 1. **Metadata**: to define basic information about your object including a title, language, description and an estimated time to complete the learning object.
 2. **Objective**: to establish what your students are going to learn, i.e., set your learning objective. You have two options for writing learning objectives: 1) using the Objective Builder in step 1 or 2) writing the learning objective without any assistance by skipping to step 2.
 3. **Content**: to organize all of the pages that makes up the content of your object. You can arrange your text and media files by choosing from a set of page layouts.
 4. **Practice**: to organize interactive exercises, simulations and quizzes so that your students can practice what they learned in the content section.
 5. **Assessment**: to provide formal assessment as indicators of achieving mastery of the learning objective you set for your students. 
 6. **Keywords**: to tag your learning objects for easy identification, classification, and discovery
 7. **Finalize**: to distribute a learning object that is ready for your students to use, you must "finalize" the object by ensuring all items in the checklist are completed.

Saving objects
===============

As you build your object, you should save your work by clicking on 'Save' in the lower right in the Obojobo editor. Do note that each time you save; a new draft will appear in the list of drafts in the repository.  

Editing objects
================
To continue editing your object, 

1. Login into the Obojobo repository 
2. Select the desired saved draft from the "Drafts" list under the "Drafts" sub-tab
3. Click on "Continue Editing"

Previewing objects
==================
To preview what the object will look like to your students, 

1. Return to the Obojobo repository 
2. Select the draft object in the listing
3. Click on "Preview"

Finalizing objects
==================
When you are finished creating your object, head to the "Finalize" section in the Obojobo editor.  Finalizing the object will turn your object into a master object which you will use as the foundation to publish one or many instances, e.g. you could publish one instance for your undergraduate course, a second instance for your graduate course, and so forth.  Do note that if your object has any incomplete sections, you will get an alert box notifying you to complete these missing sections before you can proceed.  In such case,
 
1. Click OK to return to the editor to address/correct these incomplete sections
2. Click on "Finalize" when all sections are complete
3. Close the editor and navigate to the 'Masters' sub-tab in the "My Objects" section of the repository 


Deployment: Publishing instances
================================

To publish an instance, 

1. Log into the Obojobo repository
2. Under the "Masters" sub-tab, select the master object you wish to base the creation of your new instances
3. Click on "Publish Instance". A dialog box will pop-up to specify the name of your instance, the course or program associated with your instance, and the time frame in which your instance will remain active for access.
 
After publishing an instance, Obojobo will generate a URL (link) to give to your students. To get the URL you need to provide to your students, 

1. In the Obojobo repository, go to the My Instances section
2. Select your newly created instance
3. Click on "Copy to Clipboard" under the Link section in the "Instance Details" sub-tab

**Note**: You can publish multiple instances from a single master object - there is no limit. 

Viewing scores
==============
To see the scores collected from your instance, 

1. In the Obojobo repository, open the 'My Instances' tab in the Repository 
2. Select your instance from the listing
3. Click on the 'Assessment Scores' tab to display the scores along with a frequency distribution graph. 

Downloading scores
==================
To download the scores in a CSV format,

1. In the Obojobo repository, open the "My Instances" tab in the Repository 
2. Select your instance from the listing 
3. Click on "Download Scores" under the "Assessment Scores" sub-tab 

Continuing development
======================

Once created, master objects will remain intact; however, you could continue development of objects based on a master object by following these steps:

1. Login into the Obojobo repository
2. In the "My Objects" section under the "Masters" sub-tab, select the master object you wish to use as the source of your new object
3. Click on "Edit as New Draft" to start a new draft object based on your master

Using shared objects
=====================

Obojobo facilitates collaboration among users through the "Public Library" section which contains learning objects contributed by other Obojobo users.  To use objects shared by the Obojobo community follow these steps:

1. In the repository, go to the "Public Library" section
2. Select the object you wish to use from the list of available objects
3. Click on "Publish Instance" 

**Note**: Some objects will have the option for you to create derivative works. Click on "Create Derivative Work" to receive a copy of the master object in your list of masters. This copy can then be edited and modified. 

