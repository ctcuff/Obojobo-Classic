About
======

Some objects in the 'Public Library' may allow you to modify them to better suit your needs.  You can modify objects by creating a derivative work.  This will create an exact copy of the object which you may then edit using the Obojobo Editor interface.

To create a derivative work,

1. In the Obojobo Repository, navigate to the 'Public Library' section
2. Select an object from the listing on the left-hand side.
3. Click on the purple 'Create Derivative Work' button.
4. A dialog will be presented notifying you about the functionality of creating a derivative work.  Read this dialog and press the blue 'Create Derivative Work' button if you agree to complete the procedure.
5. A second dialog will notify you that the new object was created.  Press 'OK' to be taken to the newly created object listing in the 'My Objects' section.  Read the next section for information on editing this object.

**Note** - Not every object will allow you create a derivative work.  If this button is grayed out and cannot be depressed, this means that this feature is disabled for that object.  It is up to the author or authors of the object to allow for this feature.


Editing your derivative work
============================

Once you have created a derivative work using the steps above, you can now begin editing the object.  Editing objects is covered in the [Pro Account Quick Start Guide]([~49~]).

