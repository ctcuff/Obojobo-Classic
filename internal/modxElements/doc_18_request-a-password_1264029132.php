Uses for "Request A Password"
=============================

* Initializing a new Account
* Resetting a forgotten password

Process
======

Request
=======
Visit the Viewer or Repository and locate the "Request A Password" link at the lower right hand corner of the login screen.  Enter your username [NID](http://my.ucf.edu) and your email address.
Then, click on the "Request A Password" button.

Default Email Address
=====================
The e-mail address required by Obojobo is listed as the **campus e-mail** address for students and the **business e-mail** address for faculty & staff in your [myUCF](http://my.ucf.edu).  If you are **both** a student and a faculty/staff member Obojobo will use your campus e-mail if available, otherwise your business e-mail will be used.  You can view your e-mail address by logging into myUCF and navigating to 'Staff/Student Self Service', 'Personal Information', and finally 'Email Addresses'.

<img class="mediacenter" src="/assets/images/help/student-email.png" width="600" alt="Setting your myUCF email address (Student)" />

<img class="mediacenter" src="/assets/images/help/faculty-email.png" width="600" alt="Setting your myUCF email address (Faculty)" />

Email Response
==============
Obojobo will send an email to verify your email address. The email will contain instructions on finalizing your password request. Typically, this is a link back to Obojobo with a special key allowing you to create your own password and log in.

Troubleshooting
===============

* Email clients may filter the email response as spam; make sure your spam filter is not blocking emails from noReply@obojobo.ucf.edu.
* Changes to myUCF email and NIDs can take up to 24 hours to get to Obojobo, so you may have to wait as much as a day to request a password if you recently made changes.
* If Obojobo is unable to send an email, the problem is likely to be temporary, wait a few minutes and try again.

