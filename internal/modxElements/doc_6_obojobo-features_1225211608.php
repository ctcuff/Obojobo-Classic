Obojobo is an ever evolving system. Our current time line includes many more features, many more updates, and many more improvements to make Obojobo even better. As updates come through the pipe-line, our website will be updated to let you know about the newest and greatest. In the meantime, don't hesitate to give us feedback so that we can hear it directly from you.

We hope that you enjoy using Obojobo and continue to find new things along the way as we continue to improve the system. 

