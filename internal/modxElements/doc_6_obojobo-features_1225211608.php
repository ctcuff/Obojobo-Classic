A Learning Object Platform
======

Obojobo's mission is to take the open-ended concept of learning objects and make it functional for the classroom.  In pursuing this mission, Obojobo has been built as a complete learning object platform: The repository allows you to manage and distribute your objects, the editor allows you to create them, and the viewer provides the front-end interface for your students to start learning.

Message Design Principles Built-in
======

We have worked very hard to make sure that Obojobo not only enables creators to build learning objects but also helps guide creators by informing them on the best practices for building strong content.  These guidelines have been incorporated throughout the creation process, but they are also designed to be un-obtrusive so as to not get in the way of advanced users.

Our philosophy is to help creators author the best objects they can, which in the end will strengthen Obojobo.

Collaboration Enabled
======

Obojobo allows you to build learning objects solo, but it also allows you to share and collaborate.  You can assign friends or other users to help you design a learning object.  You can even share your learning object with other Obojobo users by making it available in the Obojobo public library.  Your colleagues can then utilize your object and make it available to their students, or expand upon it by making their own changes.

Reusable
======

Learning Objects, created with the built-in guidelines in mind, are designed to represent the smallest unit of learning possible. This means that any learning object you build are not for one-time uses - they can be course-independent. You can build up your own store of objects and reuse them again and again across courses. You can dig into the public library and build a course lesson plan quickly by using objects contributed by others. 
In addition, you are welcome to modify your objects should your content need updating to keep up with changes that are pertinent to a specific field of study. The learning objects you create with Obojobo will never have to be set in stone. 

Practice Makes Perfect.  So Does Instant Feedback.
======

Since our learning object model calls for opportunities to practice, learners get to exercise the knowledge they just learned in the content area. Feedback that you design is provided to assist in the learning process, and with a small time commitment students can get a grasp of how well they comprehend the subject. If they don't understand on the first try they can jump back to the content and try again. If your students are confident that they have mastered the content, they may move on to the assessment for a formal score. 

Assessment You Can Take With You
======

Assessment scores are displayed in a simple interface. As a faculty, you will see charts to help you organize the information, with more visualization tools coming down the pipeline in future Obojobo updates. 
Scores do not have to live on Obojobo, however. You can download scores and import them in your gradebook, course management system, or whichever application you are using.  Future updates will provide support for Webcourses, making it even easier for you to integrate Obojobo in your online courses. 

Easy To Get Started
======

Obojobo was designed to be easy to use. Help and guidelines are built- in, with full documentation to back it up. In addition, we have also streamlined the process of creating objects into a step-by-step workflow. 

For students, the viewer is divided into four simple categories: Learning objective, content, practice and assessment. Starting with the objective, students get a quick idea of what they will be learning. Next, content teaches the concept(s). Practice allows them to test their knowledge, and assessment allows them to demonstrate what they have learned. If students have any issues, documentation is available for them as well. 

Powerful For Pros
======

While Obojobo was designed to be easy, it's far from simple. Adobe Captivate support means that you can create interactive practice and assessment beyond the traditional multiple choice assessment type. 

Multiple permission levels mean that you can be specific with how other users collaborate in the creation of learning objects. 
The road map planned for Obojobo will bring many more features that will enable users to make greater and more innovative content. 

Times Change.  So Does Obojobo.
