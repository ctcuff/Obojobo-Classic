Pro Account Quick Start Guide
=================================

About
=====

This guide is a supplement to the [Basic Account Quick Start Guide]([~46~]).  The Basic Account guide covers how to use objects from the public library, publish instances and collect scores.  This guide assumes that you are are familiar with these topics.

This guide is designed to provide an overview of the learning object creation process as well as how to manage the objects you create.

Pro Accounts
============

This guide is targeted towards users which have a Pro Account, and covers features and functionality that are only available for Pro Account users.  For more information on Basic and Pro accounts (and how to apply for a Pro Account), visit [this article]([~47~]).

What does Obojobo do with a Pro Account?
========================================

[Obojobo]([~8~]) is a management system to assist you in the creation and administration of [learning objects]([~13~]). With Obojobo, your students have access to purposeful and concise instructional pieces you design using the Obojobo editor.  As the author of learning objects, you are able to use the editor tools to build instructionally sound learning objects.  You can create these objects singularly or collaboratively with your colleagues with permission levels that you define.

What do I do as a Pro Account user?
===================================

As a pro account user you have the ability to author learning objects as well as manage and share them with your colleagues.  The Repository and Editor are the two interfaces you will use to create and manage your learning objects.


