Drafts & Masters
================

Now is a good time to briefly cover some terminology.  In the Obojobo system, objects can come in two forms: Drafts & Masters.

When you start creating a new learning object, it starts life as a draft.  As the name implies, a draft is an incomplete object. As such, the only people that will see your draft is you and any collaborators you decide to add to your draft object. Students will never see your draft object.

In Obojobo, each draft learning object has a corresponding version number. Each new draft begins at version 0.1.  Each time you save, your draft's version number will increase - from 0.1 to 0.2, 0.2 to 0.3 and so on.

<img src="/assets/images/help/create/draft-process.png" />

In contrast to drafts, which are evolving works in progress, masters are polished, complete, and set in stone.  Masters are suitable to be viewed by students and used in courses.

Both drafts & masters are shown in the 'My Objects' section of the Repository.

For more information on the difference between a draft, master and an instance, read the [Drafts, Masters & Instances]([~23~]) article.


Saving objects
===============

<img src="/assets/images/help/create/creator-save-button.png" />

As you build your object, you should save your work by clicking on 'Save' in the lower right in the Obojobo editor.  Each time you save, the version number of your draft will increase (0.1 to 0.2, 0.2 to 0.3, and so on).


Editing objects
===============

The above information covers how to begin a brand new object.  However, eventually you will need to close your object and come back to it later.  In addition, other Obojobo users may grant you permissions to edit their objects.  In both of these cases, you will be editing an existing object.  To do so,

1. In the Repository, navigate to the 'My Objects' tab.
2. Select the desired saved draft from the listing on the left-hand side.
3. Click on the purple 'Continue Editing' button.  This will launch the editor interface, allowing you to continue working where you left off.

While constructing your object, refer to these guides for helpful information on best practices:

* [Content]([~22~])
* [Text Guidelines]([~31~])
* [Media Assets]([~25~])
* [Practice]([~27~])

Previewing objects
==================
There are two methods of previewing the objects that you have created.  If you are in the Obojobo Repository, you can preview any object by selecting the object in the 'My Objects' section and clicking the purple 'Preview' button.

<img src="/assets/images/help/create/my-objects-preview-button.png" />

Alternatively, if you are actively editing an Object using the editor, you can use the 'Preview Student View' button in the lower right-hand corner.  

<img src="/assets/images/help/create/preview-student-view-button.png" />

Both of these buttons will open the object in the Obojobo Viewer in preview mode, which is how your students will interact with your object.


The Final Step
===============
As you continue to edit your object you will move closer and closer to completion.  When you have finished creating your object, you will need to finalize your object from a draft into a master.

When you are ready to finalize your object, navigate to the "Finish" section in the Obojobo editor.  This will open up the 'Master Checklist'.  The Master Checklist will list any problems found in your object, such as a question missing answer text or a blank content page.  You will need to go through the checklist and resolve these issues before your draft object can be finalized.  This step is required to ensure that all objects created in Obojobo are complete.

