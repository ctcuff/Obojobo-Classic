Deployment: Publishing instances
================================

Publishing instances from your objects is very similar to publishing instances from the Public Library (which is described in detail in the [Basic Account Quick Start Guide]([~46~])).  Simply select your Master object from within the 'My Objects' section, then click on the purple 'Publish Instance' button.

**Note:** You can publish instances from Master objects, but you cannot publish instances from Drafts.  If you don't see the 'Publish Instance' button then you have selected a Draft object instead of a Master.  Since Drafts are works in progress they are not complete and are not ready to be seen by students.

Viewing & Downloading Scores
============================

Viewing & Downloading Scores is covered in depth in the [Basic Account Quick Start Guide]([~46~]).

Continuing development
======================

Once created, your master objects will remain intact.  However, you may want to improve or modify your objects.  This is possible by creating a new draft series from a master object.

To do this,

1. Navigate to the 'My Objects' section of the Repository.
2. Select the master object you wish to modify.
3. Click on the purple 'Edit as New Draft' button.

This will pull up the editor, allowing you to begin working on a new version of your master object.  When you finalize your new draft object, it will become the next version (2.0, 3.0, etc) of your object.  Note that creating a new version of your master object doesn't remove the older versions.  Your 2.0 object can co-exist with version 1.0.  Should you decide to remove one master object, the other master versions will remain intact.  In addition, if you decide to delete a draft, any related masters will not be deleted.

Refer to [Drafts, Masters & Instances]([~23~]) for more information on this topic.


