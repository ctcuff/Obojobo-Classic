About
=====
The score details window allows you to see scores and responses for each attempt and each question of an instance.

Accessing the dialog
====================

To summon this dialog,

1. Select an instance from the 'Published Instances' section.
1. Navigate to the 'Assessment Scores' tab.
1. Click on the 'Details' button for the user you are interested in.

<img src="/assets/images/help/create/visit-viewer-button.png" alt="Scores Details Button">

This will bring up the window shown below:

<img src="/assets/images/help/create/about-score-details-window-1.png" alt="Scores Details Window">

Once the window is open, you have a few options.

Viewing attempt details
===========================

Clicking on a gray attempt row will show information about that attempt, including

* The attempt score
* The number of questions answered
* When the attempt was started
* When the attempt was submitted
* How long the attempt was open (the duration)

<img src="/assets/images/help/create/about-score-details-window-2.png" width="700" alt="Attempt information">

Viewing responses
============================

Clicking on a question row will show you the question along with the correct answers, incorrect answers and the recorded response.

<img src="/assets/images/help/create/about-score-details-window-4.png" width="700" alt="Viewing responses">

Grouping by questions
=====================

In addition, you can toggle the grouping between Attempts and Questions.  The default grouping (Attempts) will list the questions from each attempt starting at the first attempt.  Question grouping is useful for instances that allow for more than one assessment attempt.  This grouping better conveys how scores for each question changed over multiple attempts.  In the example image (below) with question grouping turned on, you can see that the student improved their score for question 3 over two attempts.

<img src="/assets/images/help/create/about-score-details-window-3.png" alt="Question grouping">

