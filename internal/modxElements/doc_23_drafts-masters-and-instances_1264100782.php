Drafts, Masters & Instances
===========================

The following terminology is important to know when creating learning objects in Obojobo.

Objects in the Obojobo system come in three flavors:

* **Drafts**
* **Masters**
* **Instances**

The figure below gives an overview of how these types of objects relate to one another

<img class="mediacenter" src="/assets/images/help/overview.png" alt="Figure 1: Learning object creation overview" />
<p style="text-align:center;">Figure 1: Learning object creation overview</p>


**Drafts** are converted into **masters**, and **instances** are created from **masters**.  Read on for more details on this process.

Drafts
=======

When you start creating a new learning object, it starts life as a draft.  As the name implies, a draft is an incomplete object.  As such, the only people that will see your draft is you and any collaborators you decide to add to your draft object.  Students will never see your draft object.

In Obojobo, each draft learning object has a corresponding version number.  Each new draft begins at version 0.1  Each time you save, your draft's version number will increase - from 0.1 to 0.2, 0.2 to 0.3 and so on.

<img class="mediacenter" src="/assets/images/help/draft-process.png" alt="Figure 2: The Draft Saving Process" />
<p style="text-align:center;">Figure 2: The draft saving process</p>
	
</p>


Masters
======

When you are ready to distribute your object to your students, you must create a master.  Before you can do that, you will need to take your draft and **finalize** it.

<img class="mediacenter" src="/assets/images/help/finalize-process.png" alt="Figure 3: Finalizing" />
<p style="text-align:center;">Figure 3: Finalizing</p>


Finalizing a draft will convert the draft into a master.  In contrast to drafts, which are evolving works in progress, masters are polished, complete, and set in stone.

Since masters are complete products, they are suitable to share with other Obojobo users.  Sharing is beyond the scope of this document, but if you are interested, you can find more information in the [Permissions]([~26~]) article.

To finalize your draft,

1. Login into the Obojobo repository
1. Go to the "My Objects' section
1. Select the draft object you want to finalize
1. Click on 'Continue Editing' to launch the object editor
1. Click on the 'Finalize' tab
1. Click on 'Create Master' (shown below):


<img class="mediacenter" src="/assets/images/help/create/create-master-button.png" alt="The Create Master button in the Content Creator" />

Once you have finalized your object, you can find your newly created master object under the "My Objects" section of the Obojobo repository.

Instances
=========

Now that you have created your master, you are ready to distribute your object to your students.  To do this, you will **publish instances** of your master object.  If you can imagine the master object to be the original copy, instances are analogous to photo-copies of the original.  In other words, you can create multiple instances from one master object.  This concept is demonstrated in the figure below:


<img class="mediacenter" src="/assets/images/help/publishing-process.png" alt="Figure 4: The publishing process" />
<p style="text-align:center;">Figure 4: The publishing process</p>


This means that you could create one master object, publish several instances, and distribute an instance per course section or among different courses.  To the student, your instance will look just like your completed master object, yet each instance will contain its own scores (just like each course contains its own student roster).

To publish an instance,

1. Login into the Obojobo repository
2. Click on the "Masters" tab in the "My Objects" section
3. Select your master object under the masters listing 
4. Click on the "Publish Instance" button

<img class="mediacenter" src="/assets/images/help/create/library-pub-inst-button.png" alt="The Repository's Publish Instance Button" />

Continued Development
=====================

Over time, you may come up with improvements or changes for your object.  Fortunately, while master objects are set in stone, you can still edit them.  However, once you start editing a master object, such object will begin life as a new draft.  If you are editing a 1.0 master object, a 1.1 draft object will be created.  As you continue editing your new draft, the version number will change from 1.1 to 1.2, 1.2 to 1.3 and so on.  Then, when you have completed your draft, you can finalize your object into a 2.0 master object.


There is no limit to the number of revisions - your master can evolve from 2.0 to 3.0 to 4.0 and so on.
