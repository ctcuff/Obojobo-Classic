Main Sections
-------------

The repository is split up into four primary sections:

* My Objects
* My Instances
* Media Assets
* Public Library

### My Objects

This section houses all of your objects.  "Your objects" includes any drafts you have created, any masters you have created, any objects that others have defined you as a collaborator, or any objects you have copied from the public library.  To better understand the difference between drafts and masters, please refer to the [Drafts, masters and instances]([~23~]) guide.

My Objects is the section you will see as soon as you log into the repository.  The 'My Objects' section is split into two sub-sections: 1) drafts and 2) masters.  Click on the corresponding tab to change between viewing your drafts or masters.

The 'Refresh' button will update the corresponding listing.

#### Drafts Tab

When you click on a draft object from the list of drafts on the left, you will see details about the object on the right panel.  

The purple bar at the top is the **Action Bar** which provides the following actions applicable to the selected draft:

* Preview: Opens your object in preview mode which allows you to see and experience what your students will encounter when they access your object.  Although this view is very similar to what the student will see, there are a couple of key differences:
 * In the assessment quiz, you will be able to see your answers immediately.  This is not the case for your students as they will not receive their score until they have completed and submitted the assessment.  
 * In addition, if your learning object contains any media assets, information pertaining to the available space and the size of the media is shown in the upper-left corner of every media piece.
 * Continue Editing: opens your object in the learning object editor.  The editor will open in a new tab or window.  When you are done editing your object, you can close the tab or window to return to the repository.  For more information on the editing process, please refer to the [[help:create:editor|Editor]] guide.
 * Permissions: opens up the Permissions window.  Under the 'Collaborators' tab you can add and remove collaborators to your object.  Note that you can only add or remove collaborators if you are an owner of the draft learning object.  Also, in the Ownership tab, you may remove yourself from an object as long as another owner for the object exists. Please refer to the [Permissions]([~26~]) guide for more information.  
 * Delete: deletes your learning object.  Please know that deleting will remove your draft completely, and cannot be undone.  Clicking on 'Delete' will prompt you to make sure you want to delete the object.

Below the actions bar, you have the 'Details' area which includes:

* Permissions & Ownership which lists everyone (including you) who has ownership to this draft.  You can click on 'Permissions' in the actions bar to modify permissions.
* Draft name and version number to identify the selected draft
* Object Details which displays information about your learning object to help you identify it.

#### Masters Tab

As the name indicates, the 'Masters' tab lists all of your master objects. When you click on a master object from the list of masters on the left, you will see details about the object on the right panel.  

Masters Tab Actions:

* Preview: allows you to preview your master object in the student view, similar to previewing drafts.
* Edit as New Draft: begins a new draft based on the master object you have selected.
* Publish Instance: creates an instance of your master object for each course. This feature is important as each instance will contain its own scores (just like each course contains its own student roster). For more information on instances, please refer to the [Drafts, masters and instances]([~23~]) guide.
* Permissions:opens up the Permissions window.  Under the 'Collaborators' tab you can add and remove collaborators to your object.  Note that you can only add or remove collaborators if you are an owner of the draft learning object.  Also, in the Ownership tab, you may remove yourself from an object as long as another owner for the object exists. Please refer to the [Permissions]([~26~]) guide for more information.
* Share: makes your selected master object available in the Obojobo public library for other users to be able to publish instances from your object. The master object will remain under your control.
* Delete: deletes your learning object.  Please know that deleting will remove your master object completely, and cannot be undone.  Clicking on 'Delete' will prompt you to make sure you want to delete the object.


Below the actions bar, you have the 'Details' area which includes:

* Permissions & Ownership which lists everyone (including you) who has ownership to this master.  You can click on 'Permissions' in the actions bar to modify permissions.
* Object Details which displays information about your learning object to help you identify it.

### My Instances

This section houses all instances you have created, either from your master objects or from master objects in the public library.

#### Instance Details Tab

Actions:

* Preview allows you to preview your master object in the student view, similar to previewing drafts.
* Edit Details allows you to edit the instance details instead of defining them for the first time. 
* Delete deletes your instance. Please know that deleting will remove your instance completely, and cannot be undone. Clicking on 'Delete' will prompt you to make sure you want to delete the instance.

Instance Details:

* Link provides you the URL (link) you need to give your students so that they can access your instance. Click on 'Copy to clipboard' and paste the link in the document (course web page, syllabus page, etc.) you will provide to your students.
* Instance Details which displays information about your instance to help you identify it.

#### Assessment Scores
Actions:

* Download Scores allows to download the scores of your instances in a CSV file so that you may import the scores into other applications such as Microsoft Excel.

Instance Details:

* Scores section provides scores and basic statistical data including maximum, minimum, mean and standard deviation.
* Frequency Distribution Chart provides a visual representation of how scores are distributed.


### Media Assets

This section houses any media that you have uploaded.

Actions:

* Edit Media Details allows you to edit details about the media you uploaded such as the title, description, copyright among other details. 
* Delete as the name indicates allows you to delete the selected media you uploaded. 

Page Details:

* Media Details which displays information about the media you uploaded to help you identify it.
* Preview allows to view the media to ensure you incorporate the right media into your learning objects.


### Public Library

This page is the public repository of learning objects contributed by the Obojobo community.

Actions:

* Preview opens your object in preview mode which allows you to see and experience what your students will encounter when they access your object.
* Publish Instances creates an instance of the shared object for each course. This feature is important as each instance will contain its own scores (just like each course contains its own student roster). For more information on instances, please refer to the [Drafts, masters and instances]([~23~]) guide.
* Create Derivative Work creates a copy of the master object which you may edit. This option is only available if the user who contributed the object does not mind the Obojobo community to edit the object and has enabled the feature.
* Remove From Library removes your shared object from the public library. Once the object is removed, Obojobo users will not be able to create new instances from your object; however, any instances created before you removed the object will not be removed.

Page Details:

* Object Details which displays information about the shared learning object to help you identify it.

