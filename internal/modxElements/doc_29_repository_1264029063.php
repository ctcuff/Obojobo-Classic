Publish Instance Window
-----------------------

You may envoke this window by clicking on 'Publish Instance' (under Masters in 'My Objects' or in the 'Public Library')

This window will guide you through the process of creating a new instance.  It requires the following information:

* Instance Name: This is the name of the instance, which is what students will see when they view your instance.  When you create an instance, by default this field will be the same title as the object name.
* Course: The course field is for you to be able to tag the course for which this instance will be deployed.  It is recommended that you follow the format of 'Prefix Number Section', for example 'ENC 1101 08FallW59' - the same format as Webcourses for UCF.  Future versions of Obojobo will be able to automatically populate this field with a listing of your current courses.
* Start Date: This is the date when the instance will be opened to students.  A student will not be able to access the instance before this date.
* Start Time: Likewise, students will not be able to acess the instance before this time on the specified start date.
* End Date: This is the date when the instance will be closed to students.  Students will be able to access the content and practice sections of the instance after this date. However, students will not be able to access the assessment section.
* End Time: Likewise, students will not be able to access the assessment of this instance after this time on the specified end date.
* Assessment Attempts: This is the number of tries a student may take the assessment.  Currently, the highest score received will be the score that is reported.  When viewing the instance, students will see the number of assessment attempts they have before they begin taking the assessment.

