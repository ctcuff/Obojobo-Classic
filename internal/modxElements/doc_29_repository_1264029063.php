Repository
==========

About
-----

The Obojobo repository is the main interface for authors to create, edit, and manage learning objects. The repository also provides information about students' performance in the assessment section of your learning object(s). Through the repository, Obojobo users with authoring privileges have the ability to share their learning objects with colleagues via the Public Library as well as access objects created and shared by the Obojobo community. 


Logging-In & Inactivity
-----------------------

To login, UCF faculty and staff should use their NID as their username. If you do not know your NID, you may log into myUCF to find out what your NID is. If you are not a UCF faculty or staff, your username will be assigned and emailed to you after proper credentials and affiliation to UCF are confirmed.

To set up your Obojobo account, refer to the [[Help:Accounts|Accounts]] page.  

Please note that the Obojobo repository is set to disconnect after 20 minutes of inactivity.  If you leave the repository open in a browser window and return to the repository after it has disconnected, you will be notified to log-in again.  Any information you were working on will not be lost, however.

