Public Library
==============

The Obojobo public library is where you can contribute your objects and access objects shared by the Obojobo community.

The public library is open for all users of Obojobo to use.  Every object in the public library can be used to publish instances from.  Over time the public library will grow in size and depth, allowing multiple opportunities for you to find useful content to use in your course.

Utilizing the Public Library: Creating instances
=================================================

To access the Obojobo Public Library, follow these steps:

1. Log into the Obojobo repository
1. Click on Public Library at the top. A list of available objects will display on the left

To determine if you would like to use a public object in your course, follow these steps:

1. Select the object that interests you from the list of public objects
1. Click on the 'Preview' button.  Please note that in the preview mode, you will be able to try out the practice and assessment, but no actual score will be recorded.

If you decide to use the object in your course, click on 'Publish Instance' to create an instance for your course.  


<img class="mediacenter" src="/assets/images/help/create/library-pub.png" alt="The Repository's Publish Instance Button" />

For more information on instances, read the [Drafts, Masters and Instances Guide]([~23~]). 

Creating Derivative Works
==========================

Some objects in the public library may allow for the creation of derivative works if the contributor chooses to do so.  Derivative works are essentially copies of objects. Derivative works allow you to make changes to the shared object while keeping the original object untouched.  Once you receive the copy of an object, you obtain complete control of the new (copied) object.

You will know that an object is available for you to create derivative works as the 'Create Derivative Works' button will not be grayed out.

To create a derivative work, 

1. Select the object in the listing in the Public Library
1. Click on 'Create Derivative Work'. This will copy the object and place your personal copy in your list of masters (under the 'My Objects' tab). 

Again, note that not all objects in the library will allow this feature, as derivative works are at the discretion of the contributor.  See 'Allowing Derivative Works' below for more information.

Sharing Your Own Objects
========================

In addition to utilizing objects in the public library, you can also contribute!  The public library contains all the objects that have been shared by the Obojobo community.  The more users contribute, the stronger the public library will become.  Therefore, we recommend that you share your objects when applicable.

Sharing is limited to master objects.  Since drafts are works in progress they are not appropriate to share to the public.  For more information on the distinction between drafts, masters and instances please refer to the [Drafts, Masters and Instances Guide]([~23~]).
 
To share your object, 

1. Login to the Obojobo repository
1. Go to the 'My Objects' section
1. Go to the 'Masters' tab
1. Select the object you wish to share from the 'Masters' list 
1. Click on 'Share'

<img class="mediacenter" src="/assets/images/help/create/masters-share.png" alt="The Repository's Share Objects Button" />

A dialog will pop-up to explain sharing.  Click 'Apply' to agree and share the object.

Allowing Derivative Works
=========================

When you share an object, you will have the opportunity to enable the option 'Allow Derivative Works'.  This option will allow your Obojobo colleagues to create copies of your object which they can then edit (see 'Creating Derivative Works' above) to suite their teaching needs.  Your original object will remain untouched and within your control.

This feature is useful if your colleagues would like to use your object but would like to make some changes or adapt your object to fit their needs.  Many users find this option beneficial, so we recommend that you consider enabling this option.

To allow derivative works,

1. Login into the Obojobo repository
1. Go to the 'Masters' tab under the 'My Objects' section
1. Select the object you wish to share from the list of masters
1. Click on 'Share'
1. Select 'Allow Derivative Works' in the 'Options' section 
1. Click on 'Apply'

Removal
=======

You are free to remove an object from the Public Library listing at any time.  This feature is enabled on objects that you own. You cannot remove other users' objects from the library.

To remove an object you contributed to the Public Library,

1. Login into the Obojobo repository
1. Go to the 'Public Library' section
1.  Select the object you wish to remove from the library
1. Click on 'Remove From Library'

<img class="mediacenter" src="/assets/images/help/create/library-remove.png" alt="The Repository's Remove From Library Button" />

When you remove an object from the library, from that point on, your object will no longer show up in the Public Library.  However, any instances that other users have created based on your object will continue to exist unless those users delete their instances as well.

Deletion
========

Unlike the removal of objects, the deletion of objects you have contributed to the Public Library is restricted. Since fellow Obojobo users may have published instances from your object, it is possible that such instance is still open and scores are being collected. Therefore, deleting your object could adversely affect other users.

Due to this rationale, it is important that you are satisfied with your object before you contribute it to the public library.

Deletion: Extenuating Circumstances
===================================

You may be in a circumstance where you need to delete an object from the Obojobo Public Library - for example, to comply with a copyright agreement/expiration.  If you are in a situation of this nature, please concact us at <newmedia@mail.ucf.edu>.
