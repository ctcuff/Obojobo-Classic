About Obojobo
=============
Obojobo is UCF's new online tool that provides a cohesive interface to contain, build, and utilize instructional components. These components, standard in structure and rich in media capabilities, are wrapped in an interface built from the ground-up to include help and guidelines assisting designers and authors in the development of sound and powerful instructional components.

Our vision is to provide a tool that is not only a repository but also a collaborative content creation, packaging and distribution platform to facilitate learning with great assessment capabilities.

Obojobo is the outcome of hard work by CDWS staff with the guidance and feedback from students, faculty across the state of Florida, and instructional designers.  The development team will continue to work wholeheartedly in the next set of features slated for the next release. Make sure to check on our progress at our [Twitter Page](http://twitter.com/obojobo).

