Login
=====
When accessing an Obojobo instance or the Obojobo Repository, you will be presented with a screen similar to this:

<img class="mediacenter" src="/assets/images/help/general/login-groups-viewer.gif" alt="Figure 1: Login Groups" />

You will need to select your login group to continue the login process.  If you are enrolled or are employed for UCF you should select 'UCF User Login'.  Guests of the university should select 'UCF Guest'.  If you have an internal Obojobo account (which is any user with a tilda as the first character in the username, such as '~myname'), select 'Others'.  Continue reading for more information on these login groups.

UCF User Login: UCF Student, Staff & Faculty
============================================
* This login uses **UCF NID** and **UCF NID Password** (The same password used for Webcourses).
* **UCF NID**: Your NID, or Network ID, is composed of the first two letters of your first name and a six-digit number. If your first name has only one character, the character is duplicated to create the two letters for your NID. The six-digit number is randomly generated. If you work at UCF, your NID may be different from the default format. [Determine your NID](https://my.ucf.edu/static_support/pidnidwrapper.html).
* **NID Password**: Your NID password is shared for several systems on campus including the Main Computer Labs, WebCT, Webcourses@UCF, Obojobo, and several others.  [Visit the central reset form](https://www.secure.net.ucf.edu/extranet/reset/validation.aspx?type=nid) to change your password.
* If you are having trouble logging in, resetting your NID password may help.  [Visit the central reset form](https://www.secure.net.ucf.edu/extranet/reset/validation.aspx?type=nid) to change your password.
