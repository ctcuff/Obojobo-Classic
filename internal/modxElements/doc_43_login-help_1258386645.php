Guest Login
===========
* This login is for internal Obojobo users.  Unless you have been given an internal Obojobo username, you will not need to use this login group.  All internal Obojobo accounts begin with a tilda character (for example, "~mytilda").
* **Username**: This is the username that has been supplied to you.  It should start with a tilda (~) character.
* **Password**: This is your internal Obojobo password.

Resetting Passwords
===================
If you suddenly have a problem logging into Obojobo, most likely your password was reset.  If you find that you can no longer login to Obojobo (but you can still login to Webcourses), your passwords for these systems are most likely out of sync.  Resetting your password for Obojobo will change the password for both systems, allowing you to login again.  [Visit the central reset form](https://www.secure.net.ucf.edu/extranet/reset/validation.aspx?type=nid) to reset your password.

