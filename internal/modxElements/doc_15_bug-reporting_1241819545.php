Bug Reporting
=============

Workarounds
-----------

Be sure to check our [known issues]([~7~]) page first!  Your issue may have a solution in the works.

For some bugs, refreshing your browser window may solve the problem.

For all bugs you discover that are not shown in the [known issues]([~7~]) page, we urge you to report them.

Submitting Bug Reports
----------------------

In the Repository, there is a Feedback button in the upper left.  Click this to open up the Submit Feedback/Bug Report window. 

For bug reports, click "Bug Report" and begin describing your problem.  You may also use this window to give general feedback or give suggestions for features you'd like to see.

Example Bug Report
------------------

The more detailed you are in the report, the more useful your report will be.  It will be beneficial to us for you to describe what you were doing as well as the bug itself.  An example is shown below:

*I closed the help in the repository by clicking on the up-arrow button, but the button disappered and I could not get it back.  When this happens, I have to refresh the browser to get the arrow back.  This only seems to happen after closing and opening the help multiple times.*

(Note: This is an example report - This was not taken from an actual report).

When can I expect a fix?
------------------------

Unfortunately, we cannot promise that every issue will be fixed immediately.  We will need to review the problem and work on a solution. Each problem will be prioritized depending on severity and order in which is submitted.  We ask for your patience and sincerely apologize for the inconvenience. Most of all, we thank you for your support as you help us improve Obojobo.  Check out the [updates & new features]([~9~]) page which will be updated when bugs are fixed or new features are added to Obojobo.

