Media Assets in the Repository
==============================
Obojobo supports various media types, for specific information check out the [supported media]([~30~]) page.

"Media Assets" houses all of the media you have uploaded in Obojobo.  Navigating to this section will show you a list of your media, organized by name, file type, and upload date.  This document outlines some guidelines for creating and using media in objects you author in the Obojobo system.

Media Guidelines
=========================

 * File size always matters - The smaller media is, the faster it can be displayed.
 * Screen sizes - Your students will have an array of different screen sizes.  We recommend designing for a **1024x768** resolution.  This is also the minimum resolution supported for the Obojobo Viewer (the interface your students will use).  If you do offer larger media, make sure they are still usable at a scaled down ratio.
 * Scaling Media - Obojobo will scale your content to fit the viewer's screen if needed.  This may distort your images or make text difficult to read.  You should try to size your media so that it works for your viewers.

<img class="mediacenter" src="/assets/images/help/create/previewing-media-size.png" width="375" alt="The viewer offers some help measuring content sizes" />

Images
-------
Images are a media type that can be incorporated within the [content]([~22~]) section of our [learning objects model]([~13~]).

### Uses

* Illustrate, organize, or explain online content
* Make pages visually appealing
* Clarify your point(s)

### Suggestions

* Superfluous decorative graphics can clutter your page and distract users from the content
* Use the best image format for the job, for instance JPEGs are typically better for photos than PNGs.
* Make sure content isn't obscured as the content scales down to fit smaller screens.
* Don't use images as a replacement for text, using text will make your content easier to output, reuse, and search.

### Accessibility

Understanding the needs of learners with different disability types and complying with accessibility requirements are essential when incorporating graphics into your learning objects. For instance:

* Understanding how different colors work together in conveying a message is relevant for the visual learners in your course.
* Including alternative text for images will allow users with visual impairments access to the content with screen readers.

Video
-----
Video is supported, either through a .flv file or through a YouTube video.

Flash Animations and Interactive Media
--------------------------------------
Currently, Obojobo supports animations and interactive media saved as SWF files (Adobe Flash, Captivate, etc.)

### Uses
* Animations may be used to visually demonstrate a process (sequence of steps), motion, or change over time.
* Animations are useful to convey complex concepts 
* Interactive media could be used to provide hands on experiences dealing with decision making, cause-and-effect, and exposure to new procedures.
* Indicate dimensionality in transitions: e.g. zooming on an object to show growth.  
* Enhance graphical representations: e.g. teaching how to use software

### Suggestions
* Keep animations simple, effective, and concise.  Animation for animation's sake will often turn off a learner's interest.
* Use Flash 8 and earlier sparingly especially if the animation has complex [ActionScript](http://en.wikipedia.org/wiki/Actionscript).  Obojobo may not be able to load some older Flash content (Flash 8 and earlier).
 

### Accessibility

* Use the Accessibility panel in Adobe Flash which allows the designer and developer to add names and descriptions similar to the "alt" and "longdesc" used for images in HTML. 
* Provide text equivalents to buttons so that assistive technology devices can identify and read aloud the navigation options 
* Provide keyboard shortcuts to navigate or interact with the Flash media
* Provide the ability to mute music or audio that may distract your learners

