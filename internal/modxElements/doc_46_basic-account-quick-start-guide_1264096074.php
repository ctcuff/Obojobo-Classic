Editing Details
---------------

<img src="/assets/images/help/create/edit-inst-details.png" />

Once you create your instance it is saved along with all of the options you defined when you first created it.  If you wish to change these options (for example, increase the number of assessment attempts or change the assessment due date), click on the purple 'Edit Details' button in the 'Instance Details' tab in the 'Published Instances' section.  This will bring up the Edit Details dialog window which will have the same fields as the window used to create your instance.  Edit the values, then click on the blue 'Save' button to commit the changes.  The interface should update, showing you the new details for the instance selected.

Scores
======

Viewing Scores
--------------

After you have assigned your instance or instances, students should begin completing assessment attempts.  Obojobo collects these scores automatically.  At any time you may review the scores collected so far.  To do so,

1. Navigate to the 'Published Instances' section of the Repository.
1. Select the Instance for which you want to view the assessment scores.
1. Click on the 'Assessment Scores' tab at the top of the Published Instances section.

<img src="/assets/images/help/create/assessment-scores-tab.png" />

This will bring up the Assessment Scores pane which features all student scores recorded thus far.  Also included is a frequency distribution chart which allows you to see how performance at a glance.

Downloading Scores
------------------

You can download scores into a CSV file for importing into other applications.  To do so, click on the purple 'Download Scores' button above the student score listing.

<img src="/assets/images/help/create/download-scores.png" />

This CSV file can be imported into your Webcourses gradebook.  Read [Importing Obojobo Scores into Webcourses]([~52~]) for a step-by-step tutorial on how to do this.

Viewing Score Details
---------------------

The Assessment Scores pane gives you final scores only, however, the Score Details feature allows you to dig deeper to see responses, question scores and attempt scores for a user.  Clicking on the 'Details' button beside the final score will open the Score Details dialog.

<img src="/assets/images/help/create/visit-viewer-button.png" />

Refer to [Score Details]([~57~]) for full documentation for this feature.

Incomplete Assessments
----------------------

When students take an assessment attempt, they are required to submit it when they are done answering questions.  Occasionally, a student forgets to actually submit the assessment attempt.  Unsubmitted assessments do not impact the final score, however, you can use the 'Score Details' feature to see assessments in progress or assessments that were never submitted.

Visit Visualizer
---------------

<img src="/assets/images/help/create/open-visit-viewer.png" />

The Visit Visualizer tool allows you to see how a student progressed through an instance, visit by visit and page by page.  You can launch the visit visualizer from the Score Details window.

<div style="margin: 0 auto; text-align: center;">
<img src="/assets/images/help/create/visit-viewer.png" />
<p style="font-size: 8pt; font-style: italic;">The Visit Visualizer</p>
</div>

Viewing Scores By Question
--------------------------

The Scores By Question is a powerful feature that allows you to see every recored response for every question in the instance.  You can launch the Scores By Question feature by clicking on the purple button above the student score listing.

<img src="/assets/images/help/create/view-scores-by-question.png" />

<img src="/assets/images/help/create/score-by-question-graph.png" />

This feature can be useful to see which questions students had the most difficulty with and how they responded.  The overview tab lets you see a graph of which answers were more commonly chosen, while the student responses tab provides a list of each recorded response.

Additional Attempts
-------------------

You may be in a situation where you would like to grant additional assessment attempts for one or more users, but not for all users.  For example, a student may complain that they submitted an assessment attempt accidentally and would like to take the assessment again.  By granting this student an additional attempt, they can receive another chance.  To do this, navigate to the 'Assessment Scores' tab for the instance you are interested in, then click the 'Edit' button which will bring up an input control.  Use the input control to define how many additional attempts you would like to grant, then click 'Apply' to commit the operation.

<img src="/assets/images/help/create/additional-attempts.png" />

The total number of assessment attempts available to a student is the sum of the number of attempts for that instance plus the number of additional attempts that you have granted.

**Note:** If you would like to grant additional attempts to all users, instead use the 'Edit Details' feature to simply change the number of assessment attempts available for the instance.  See the 'Editing Details' section discussed earlier in this guide.

Advanced Features
=================

Sharing Instances
-----------------
The Instance Managers feature allows you to share control to your instance.  This also includes the ability for other users to view scoring data.  To add Instance Managers to your instance, select an instance and click on 'Managers'.

<img src="/assets/images/help/create/instance-managers-button.png" />

Creating Derivative Works
-------------------------
Some objects in the Public Library grant you the option to make minor modifications to them by producing a derivative work.  Read [Creating Derivative Works]([~48~]) for more information.

Authoring Content
-----------------
Pro account users have the additional ability to author their own learning objects.  If you would like to author your own content, sign up for a pro account by visiting [this form]([~42~]).  Read [Basic & Pro Accounts]([~47~]) for more information on these two different account types.


