Logging-in
==========

If you are a faculty or staff member at UCF you should already have access to Obojobo.  You will need to provide your UCF NID and NID Password to gain access.  To login,

1. Open the Repository by clicking the link on the front of the Obojobo website (or, [click here](/repository))
1. If you are not already logged in, you will be asked to choose your login group.  Click on 'UCF User Login'.
1. Provide your UCF NID and NID Password.

If you are having issues logging in, the probable cause is that you need to reset your NID Password.  Visit the [NID Password Reset Page](https://www.secure.net.ucf.edu/extranet/reset/validation.aspx?type=nid) to do this.  For more help logging in, visit the [FAQ on Logging In]([~16~]) or the [Login Help]([~43~]) page.

The rest of this guide assumes that you are logged into the Repository.  If you are following along, go ahead and login before reading the rest of this guide.

