Distribution
============

Once you have found an object or objects you would like to use, you will need to publish an instance to utilize it in your course.

Instances
---------

In Obojobo terms, an *instance* is a special type of object that essentially is a copy of a master learning object, much like a compact disc or vinyl record is a copy that was pressed from an original master recording.  The objects listed in the Public Library are like the Master recordings - these are the pristine originals that are not to be used directly.  Instances, on the other hand, are packaged and ready to be used & distributed.

<img src="/assets/images/help/create/master-recording-and-pub-instance.png" />

Publishing Instances
--------------------

Creating an instance is referred to as 'Publishing an Instance'.  Why is this step required?  Instances are what students will access, which means that the assessment scores collected will be connected to that instance.  Since you wouldn't want scores from multiple classes or programs to be lumped together into the same instance, instances created in the Obojobo system are targeted for one specific use.  In other words, from one master object you can create multiple different instances - one for each course, section, semester, program, etc.

In addition to recording scores, instances have some options which you set when you publish an instance.  These include a name, a time period for when a student can access the instance and when the assessment is available, the number of assessment attempts available to students, and the way in which Obojobo will calculate the final score.

<div style="margin: 0 auto; text-align: center;">
<img src="/assets/images/help/create/about-instances.png" />
<p style="font-size: 8pt; font-style: italic;">Each instance contains its own scores and options</p>
</div>

These are options that are provided to help you best utilize the object in a way that meets your demands and integrates with your course/syllabus.  Note that these options describe one specific instance, so each instance you create can have different settings.

To publish an instance,

1. Navigate to the 'Public Library' section in the Repository
1. Select an object from the Public Library.
1. Click on the purple 'Publish Instance' button.  <img src="/assets/images/help/create/library-pub-inst-button.png" />

This will bring up the Publish Instance dialog (shown below), prompting you to define the options for this instance.  Complete all fields, noting the help presented on the right-hand side of the dialog window which will guide you through the process.  When finished, press the blue 'Publish' button.

<img src="/assets/images/help/create/pub-inst-window.png" />

You should now be presented with the 'Distribute Instance' window, providing you with the instance link.

<img src="/assets/images/help/create/instance-distribute-window.png" />

This important link is what you will provide to students in order for them to access your instance.  If you close this window, note that this link (along with all of the information you entered when creating the instance) is kept in the instance record.

To view an instance record,

1. Navigate to the 'Published Instances' section in the Repository.  This section contains every instance you have published.
<img src="/assets/images/help/create/repo-pub-instances-section.png" />
1. Select the instance from the listing on the left-hand side.  The interface will update to display the information about this instance. 

From here, you can click on the 'Distribute' button to reopen the 'Distribute Instance' window.
<img src="/assets/images/help/create/instance-distribute-button.png" />

