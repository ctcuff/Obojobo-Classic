Basic Account Quick Start Guide
=================================

About
=====

This guide is designed to get you started using the Obojobo system in your course or program.

What does Obojobo do?
=====================

[Obojobo]([~8~]) is a management system created to assist you in utilizing [learning objects]([~13~]) in your course or institutional programs.  With Obojobo, your students have access to purposeful and concise instructional pieces built using the Obojobo editor.  These objects include content, practice and a scored assessment, all tied together by a learning objective built with instructional design principals.  Each object created in Obojobo has a consistent interface, meaning that it only takes one object to become familiar with the interface.  Each object you assign includes scored assessment which Obojobo collects and reports automatically.  In addition, visualization tools allow you to dig deeper and see exactly how students interacted with the objects you assign.

For more information on the Obojobo system, read [About Obojobo]([~8~]) or [Obojobo Features]([~6~]).

