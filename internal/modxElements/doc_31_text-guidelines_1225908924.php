Length
======
If the content consists of text, the 5-15 minutes range should be based on the average reading speed of 200-250 words per minute.

Chunking
======
Organize and chunk your content in one screen sections (maximum 250 words/screen) to facilitate reading and interaction with the content. Please remember this is not the time to write a book chapter or module.  Choose carefully what you want to write so that you don't end up with 15 minutes of text alone.  

Tone
======
Although writing style differs from person to person, a conversational tone is appropriate in the design of learning objects to maximize learner engagement and time on task.  

