Student Quick Start Guide
=========================

About
-----

This document will explain the Obojobo system and give you a quick run-through of how to use Obojobo.

What is this link I received?
-----------------------------

When your professor creates [learning objects]([~13~]) with the UCF Obojobo system, you will be directed to a link (URL) whether by email or from any of the areas in your online course such as a module, course mail, discussion board, etc. Once you click on the link, you will be prompted to login using your NID and NID password. To learn about logging in or troubleshooting login problems with Obojobo, please refer to the [FAQ]([~16~]) or [Login Help]([~43~]) page. 

What do I need?
--------------

To access a learning object, you will need the following:

* Internet connection
* Link to the learning object your professor has assigned to you
* A modern web browser with a recent version of the Flash Player and JavaScript enabled.  View the [Software Requirements]([~17~]) for the most current software requirements.
 
What do I do to access the learning object?
-------------------------------------------

1. Click on the link (URL) your professor gave you
2. At the login page, type your username (NID for UCF students and faculty) and password
3. Click on "Login to Begin"

**Note:** In the login page you will see the range of time that the learning object is available for access. Please remember that after the end date, you will not be able to take any more assessment attempts.  You will be able to still access the content and practice sections, however. 

Logging-in
----------

Refer to the [FAQ on Accounts]([~16~]) or the [Login Help]([~43~]) page for information on logging in.

Viewing my object
-----------------

Once you login, you will see that each learning object is split into four primary sections:

* Learning objective  - to explain what you will be learning
* Content  - the lesson itself
* Practice - to gauge where you are in mastering what you learned in the content area
* Assessment - for a final score to demonstrate that you have mastered what you were supposed to learn. 
 
Click on the large buttons (Overview, Content, Practice, and Assessment) at the top to navigate through the different sections. 

You will notice that the practice and assessment sections have a front-page that will explain any details you need to know before you proceed, so read carefully! 

The practice section, as the name indicates, is intended for you to practice as many times as you need to learn the material. Once you are comfortable with what you learned and practiced, move onto the assessment section for a final score to be submitted to your professor. 

When you are finished, click on the "Submit Assessment" button.  If you have not answered all of the questions, a notice will pop-up asking if you are sure you want to submit your assessment with unanswered questions.  Once you complete the assessment and receive your score, you may close the learning object. 

More Information
----------------

Please refer to our [FAQ]([~16~]) page for answers to frequently asked questions.
