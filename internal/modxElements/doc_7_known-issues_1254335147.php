Known Issues
============

About
======

This page covers any known issues with Obojobo.  Issues are separated among each interface.  Refer to this page before you submit any bug reports, as we already may be aware of your issue.

If any workaround is available for the known issue, it will be listed along with the issue.

When issues are resolved, they will be removed from this page.  Check the updates page for information on issues that are fixed.

Repository
==========
* A minor problem arises if you delete all instances created from a learning object, then attempt to delete the learning object.  In this suitation you will receive a warning that you cannot delete the object because instances have been created from that learning object (even though no instances created from the object remain).  Workaround: Attempting to delete the object at a later time will successfully delete the object.
* When previewing media containing audio, the audio may continue playing.  Workaround: Stop audio before you navigate away from the media, or refresh the browser.

Editor
======

* If a user is editing an object and closes the browser without first clicking on the 'Close' button, that object will remain locked for a short period of time.  This only affects objects where collaborators have been specified.  Workaround: Click the 'Close' button before you close the browser window, or wait for the lock to timeout.

Sets
=====
* Sets HTML markup may not be XHTML valid if the a Learning Object's Objective contains non-standard html tags.
