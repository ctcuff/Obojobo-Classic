Overview
========
The content section of the [learning objects model]([~13~]) is where you convey the message to help your students achieve the learning objective you established. 

Content may take many forms, including text, graphics, audio, video, animations, interactive media, or a combination of any of these forms. The key is to select the most appropriate medium to convey the facts, concepts, processes, procedures and/or principles of the subject matter in a direct and concise manner. 

Below are some general guidelines to keep in mind when developing content, followed by specific guidelines for each media type.

General Guidelines
==================

Length
------
A good rule of thumb is to provide between 5-15 minutes worth of content per learning object, with no more than 2-3 media types at a time.

Diversity of Media
------------------
Try to provide enough detail in your content without using too much of one media type, as this can become overwhelming to the learner. 

Consistency
-----------
Be consistent in how the information is organized throughout the learning object. This includes the layout, tone, organization of content, and instructional techniques.

Whet students' appetite
------------------------
Try developing a plot for your content, lead your learners on a journey that fosters interest and encourages active involvement with your subject.  Story writing techniques can turn content delivery into a more engaging experience.  For example try adding a subtle plot twist or plant something in your explanation that is later revealed to have great significance or a deeper meaning.

