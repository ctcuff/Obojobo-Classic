Getting Technical Support
=========================

If you're having login issues, first read [Login Help]([~43~]) or the [FAQ on Logging In]([~16~]) first.  Your problem could be as simple as a NID Password reset.

If this does not solve your issue or you have another issue, read below to determine who you should contact to receive tech support.

Faculty
=======

If you are a faculty or staff member using Obojobo to access the [Information Literacy ("Info-Lit") Modules](http://infolit.ucf.edu/), you will need to contact the Ask a Librarian service.  This service can be reached at [http://library.ucf.edu/Ask/](http://library.ucf.edu/Ask/)

If you are a faculty or staff member using Obojobo outside of the Info-Lit Modules, you will need to contact Instructional Technologies Group (ITG).  You can submit a support request here: [http://teach.ucf.edu/support/get-help/](http://teach.ucf.edu/support/get-help/)

Students
========

If you are a student using Obojobo to access the [Information Literacy ("Info-Lit") Modules](http://infolit.ucf.edu/), you will need to contact the Ask a Librarian service.  This service can be reached at [http://library.ucf.edu/Ask/](http://library.ucf.edu/Ask/)

If you are a student using Obojobo outside of the Info-Lit Modules, you will need to contact the Techrangers.  You can submit a support request via Ask the Techrangers at [http://learn.ucf.edu/support/get-help/](http://learn.ucf.edu/support/get-help/)
