UCF Affiliates
==============

General
--------
UCF affiliates will have Obojobo accounts that are automatically generated based on your [myUCF](https://my.ucf.edu/) account. Use your UCF NID as your username, and your UCF preferred email address as your email for Obojobo. Unfortunately, your password cannot be synchronized with the myUCF system, so you will have to [request a password]([~18~]) on your first visit. 
Note that changes to your myUCF account can take up to 24 hours to get to Obojobo (like changing your primary email address). 

Students
--------
By default, students have access to view learning objects sent to them by a faculty member.

Faculty & Staff
---------------
Faculty and staff have access to view learning objects as well as create them. You can use the Repository to create, publish, and manage learning objects for your students. 

Other Users
============
Obojobo can handle users who do not meet the above affiliation/qualifications. If you are interested in gaining access to Obojobo, you can contact [CDWS](http://cdws.ucf.edu/) for more information. 

