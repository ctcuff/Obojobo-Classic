Overview
========

Learning Objects are essentially defined as any reusable chunk of educational material.  Generally, these are fairly small in scope and size, pertaining to a very specific concept.   However, if you read about [[http://en.wikipedia.org/wiki/Learning_object|learning objects on Wikipedia]] you can see that the term can lead to some confusion.

Learning Objects at UCF
=======================

We needed to bring learning objects to UCF in a way that was systematic, effective, and innovative.  The ground work started with a peer reviewed journal article [Practical Guidelines for Learning Object  
Granularity from One Higher Education Setting](http://ijklo.org/Volume1/v1p163-179Thompson.pdf) written by CDWS employees associated with the Obojobo project.  

The article outlines the concepts that Obojobo was engineered to support.

Learning Objects in Obojobo
===========================

In our model, learning objects consist of the following elements:

1. **Learning objective** - Each learning object can address only one learning objective.  Such learning objective must state the following to the learner: 
 1. Task: What will the learner perform or complete? 
 2. Conditions: Under which conditions should the learner complete the objective? 
 3. Criteria: To what degree should the learner achieve this objective?  
2. **Content** - these are some of the considerations regarding content: 
 * Content in a learning object should be succinct and direct; to the point. 
 * Content may be in the form of text, audio, video, interactive media or a combination of any of these.  
 * Organize and chunk your content in one screen sections (maximum 250 words per screen) to facilitate reading and interaction with the content.   
 * Content should include no more than five minutes of any one media type at a time. 
 * Establish a high-to-low level of importance for how you want to organize the content and maintain this approach throughout the design of the learning object. 
 * Be consistent in how the information is organized throughout the learning object. 
 * Text, video, audio, images or interactive media that convey the facts, concepts, processes, procedures and/or principles of the subject matter should be included.
 * Although writing style differs from person to person, a conversational tone is appropriate in the design of learning objects to maximize learner engagement and time on task.  
 * Consider the inclusion of outlines with main or key concepts and principles; however, bulleted items as used in PowerPoint presentations are not enough. Remember, students will be accessing these learning objects on their own.  You will not be there to expand on the bulleted items.  
 * Some methods of organizing your content are: overviews, vocabulary, descriptions demonstrations, diagrams, flowcharts, concept maps, and examples (good and/or bad). 
3. **Practice** - A learning object provides opportunities for learners to review facts, key concepts and principles through exercises, instructional games, simulations, problem solving and guided reflections. UCF provides a few customizable instructional games and applications that could be incorporated into the practice section of learning objects. Alternatively, practice may be provided through quiz-type self-tests (i.e., multiple choice, true-or-false, etc.) 
4. **Assessment** - A learning object should assess whether the learner has achieved the stated learning objective. Learning object authors have the choice of using traditional assessment methods such as quizzes (i.e., multiple choice, true-or-false, etc.) or non-traditional methods such as games and simulations.  

